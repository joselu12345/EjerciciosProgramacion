package capitulo05.bloque06;

public class Ejercicio02 {
	
	public static void main(String[] args) {
		
		int i, j = 0;
		char matriz[][] = new char[30][10];
		
		for (i = 0; i < matriz.length; i++) {
            for (j = 0; j < matriz[j].length; j++) {
                
                System.out.print("*");
            }
            System.out.println(" ");
        }
		
		
	}

}
