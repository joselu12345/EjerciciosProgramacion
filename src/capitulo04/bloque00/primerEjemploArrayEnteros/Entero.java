package capitulo04.bloque00.primerEjemploArrayEnteros;

public class Entero {
	
	private int entero;

	public Entero() {
		super();
	}

	public Entero(int entero) {
		super();
		this.entero = entero;
	}

	public int getEntero() {
		return entero;
	}

	public void setEntero(int entero) {
		this.entero = entero;
	}

	public String toString() {
		return "Entero [entero=" + entero + "]";
	}
	
	

}
